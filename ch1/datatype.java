class datatype
{
	public static void main(String args[])
	{
		int a;
		float b;
		double c;
		char d;
		String e;
		
		a=1;
		b=1.5f;
		c=2.5f;
		d='x';
		e="parth";
		
		System.out.println("\nint ="+a);
		System.out.println("float ="+b);
		System.out.println("double ="+c);
		System.out.println("char ="+d);
		System.out.println("string ="+e);
	}
}